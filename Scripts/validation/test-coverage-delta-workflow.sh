    fi

    # Test explicit input source
    local json_explicit="$TEST_DATA_DIR/baseline_explicit.json"
    generate_coverage_delta_json "17.0" "16.0" "1.00" "improved" "explicit_input" "false" "$json_explicit"

    if [[ -f "$json_explicit" ]]; then
